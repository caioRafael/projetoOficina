    private void botaoApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoApagarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaPessoa.getSelectedRow();            
        
         if(linhaSelecionada < 0 ){
        JOptionPane.showMessageDialog(this, "Selecione!");
        
         }
        int idPessoa = (int) tabelaPessoa.getValueAt(linhaSelecionada,0);
        
        PecaDAO p = new PecaDAO();
        
        p.delete(idPessoa);
        JOptionPane.showMessageDialog(this, "Apagado!");
        
        popularTabela();
    }//GEN-LAST:event_botaoApagarActionPerformed

    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed

     if(tabelaPessoa.getSelectedRow() != -1){
        String nome = campoNome.getText();
        String imagem = campoImagem.getText();
        String estoque = campoEstoque.getText();
        String preco = campoPreco.getText();
       

        
        Peca p =  new Peca();
        
        p.setNome(nome);
        p.setImagem(imagem);
        p.setEstoque(estoque);
        p.setPreco(preco);
        p.setId_peca((Integer) tabelaPessoa.getValueAt(tabelaPessoa.getSelectedRow(), 0));
        
        PecaDAO pessoa= new PecaDAO();
        pessoa.update(p);
        
        JOptionPane.showMessageDialog(this, "Atualizado!");
        
        campoNome.setText("");
        campoImagem.setText("");
        campoEstoque.setText("");
        campoPreco.setText(""); 
        
        popularTabela();
     
     }
   
        
    }//GEN-LAST:event_botaoAtualizarActionPerformed

