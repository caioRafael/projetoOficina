    public void popularTabela(){
        PecaDAO peca = new PecaDAO();
        List<Peca> lista =  peca.getAll();
        
        DefaultTableModel modelo= (DefaultTableModel) tabelaPessoa.getModel();
        
        modelo.setRowCount(0);
        
        for(Peca p : lista){
               
           modelo.addRow(new Object[]{
               
               p.getId_peca(), 
               p.getNome(),
               p.getPreco(),
               p.getEstoque()});
            
        }
        
    }
    
    public void popularTabelaBusca(String desc) {
        PecaDAO peca = new PecaDAO();
        List<Peca> lista =  peca.getAllNome(desc);
        
        DefaultTableModel modelo= (DefaultTableModel) tabelaPessoa.getModel();
        
        modelo.setRowCount(0);
        
        for(Peca p : lista){
               
           modelo.addRow(new Object[]{
               p.getId_peca(), 
               p.getNome(),
               p.getPreco(),
               p.getEstoque()});
            
        }
        
    }
    
   

